# Define a variable
x <- 10

# Example 1: if statement
if (x > 0) {
  print("x is greater than 0")
}

# Example 2: if-else statement
if (x > 0) {
  print("x is greater than 0")
} else {
  print("x is less than or equal to 0")
}

# Example 3: if-else if-else statement
if (x > 0) {
  print("x is greater than 0")
} else if (x < 0) {
  print("x is less than 0")
} else {
  print("x is equal to 0")
}
